/** Opens the report in a new window and triggers the print dialog (Save as PDF). */
export const exportPdf = (report) => {
  const win = window.open('', '_blank');
  if (!win) {
    window.alert('Please allow pop-ups to export the PDF.');
    return;
  }
  win.document.write(
    `<!doctype html><html><head><meta charset="utf-8"><title>${esc(report.title)}</title><style>${STYLES}</style></head><body>${buildBody(report)}</body></html>`
  );
  win.document.close();
  win.focus();
  // Give the new document a tick to lay out before printing.
  setTimeout(() => win.print(), 350);
};

/** Downloads the report as a Word-openable .doc file. */
export const exportDoc = (filename, report) => {
  const html =
    `<html xmlns:o="urn:schemas-microsoft-com:office:office" xmlns:w="urn:schemas-microsoft-com:office:word" xmlns="http://www.w3.org/TR/REC-html40">` +
    `<head><meta charset="utf-8"><style>${STYLES}</style></head><body>${buildBody(report)}</body></html>`;
  const blob = new Blob(['﻿', html], { type: 'application/msword' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename.endsWith('.doc') ? filename : `${filename}.doc`;
  a.click();
  URL.revokeObjectURL(url);
};
