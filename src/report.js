// Dependency-free finance report export: a branded HTML document that can be
// printed to PDF or downloaded as a Word file. No libraries, so nothing to
// install or keep in sync — the same markup drives both formats.

const esc = (v) =>
  v == null
    ? ''
    : String(v).replace(/[&<>"]/g, (c) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;' }[c]));

/**
 * Builds the report's inner HTML (heading, meta, summary tiles, table).
 * columns: [{ key, label, align }], rows: array of objects keyed by column.key.
 */
const buildBody = ({ title, subtitle, meta = [], summary = [], columns = [], rows = [], note }) => {
  const metaHtml = meta.length
    ? `<div class="meta">${meta.map((m) => `<span><b>${esc(m.label)}:</b> ${esc(m.value)}</span>`).join('')}</div>`
    : '';

  const summaryHtml = summary.length
    ? `<table class="summary"><tr>${summary
        .map((s) => `<td><div class="s-label">${esc(s.label)}</div><div class="s-value">${esc(s.value)}</div></td>`)
        .join('')}</tr></table>`
    : '';

  const head = columns
    .map((c) => `<th style="text-align:${c.align || 'left'}">${esc(c.label)}</th>`)
    .join('');

  const body = rows.length
    ? rows
        .map(
          (r) =>
            `<tr>${columns
              .map((c) => `<td style="text-align:${c.align || 'left'}">${esc(r[c.key])}</td>`)
              .join('')}</tr>`
        )
        .join('')
    : `<tr><td colspan="${columns.length}" class="empty">No records for this period.</td></tr>`;

  return `
    <div class="report">
      <div class="brand">MR&nbsp;<span>BITES</span></div>
      <h1>${esc(title)}</h1>
      ${subtitle ? `<p class="sub">${esc(subtitle)}</p>` : ''}
      ${metaHtml}
      ${summaryHtml}
      <table class="grid">
        <thead><tr>${head}</tr></thead>
        <tbody>${body}</tbody>
      </table>
      ${note ? `<p class="note">${esc(note)}</p>` : ''}
      <p class="foot">Generated by MR Bites · ${esc(new Date().toLocaleString('en-IN'))}</p>
    </div>`;
};

